package net.cdn.Objects;

public class FLOWRObject {

	private String sFor = null;
	private String sLet = null;
	private String sOrder = null;
	private String sWhere = null;
	private String sReturn = null;

	public String getsFor() {
		return sFor;
	}

	public void setsFor(String sFor) {
		this.sFor = sFor;
	}

	public String getsLet() {
		return sLet;
	}

	public void setsLet(String sLet) {
		this.sLet = sLet;
	}

	public String getsOrder() {
		return sOrder;
	}

	public void setsOrder(String sOrder) {
		this.sOrder = sOrder;
	}

	public String getsWhere() {
		return sWhere;
	}

	public void setsWhere(String sWhere) {
		this.sWhere = sWhere;
	}

	public String getsReturn() {
		return sReturn;
	}

	public void setsReturn(String sReturn) {
		this.sReturn = sReturn;
	}

}
